package trab1;

public class FuncionarioComun {
	private String Nome ;
	private String DataDeNascimento;
	private String DataDeIngresso;
	private String Banco;
	private int Agência;
	private int ContaCorrente;
	private float BancoDeHoras;
	private float Salário ;
	
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getDataDeNascimento() {
		return DataDeNascimento;
	}
	public void setDataDeNascimento(String nas) {
		DataDeNascimento = nas;
	}
	public String getDataDeIngresso() {
		return DataDeIngresso;
	}
	public void setDataDeIngresso(String ing) {
		DataDeIngresso = ing;
	}
	public String getBanco() {
		return Banco;
	}
	public void setBanco(String string) {
		Banco = string;
	}
	public int getAgência() {
		return Agência;
	}
	public void setAgência(int i) {
		Agência = i;
	}
	public int getContaCorrente() {
		return ContaCorrente;
	}
	public void setContaCorrente(int i) {
		ContaCorrente = i;
	}
	public float getBancoDeHoras() {
		return BancoDeHoras;
	}
	public void setBancoDeHoras(float bancoDeHoras) {
		BancoDeHoras = bancoDeHoras;
	}
	public float getSalário() {
		return Salário;
	}
	public void setSalário(float salário) {
		Salário = salário;
	}
	
	

}
